    private void loginButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_loginButtonMouseClicked
        // TODO add your handling code here:
        int loginStatus;
        String accountString;
        String passwordString;
        accountString = accountTextField.getText();
        passwordString = String.valueOf(passwordField.getPassword());
        loginStatus = uciDataBase.checkPassword(accountString, passwordString);
        if(loginStatus>0)
        {
            loginStatusLabel.setText("You have successfully logged in");
            mainTabbedPanel.setEnabledAt(0, false);
            mainTabbedPanel.setEnabledAt(1, false);
            mainTabbedPanel.setEnabledAt(2, true);
            mainTabbedPanel.setSelectedIndex(2);
            //load the course and student data into the courseTable
            refreshTables();
        }
        else
        {
            loginStatusLabel.setText("Your account/password cannot match. Please re-enter them.");
            mainTabbedPanel.setEnabledAt(0, true);
            mainTabbedPanel.setEnabledAt(1, true);
            mainTabbedPanel.setEnabledAt(2, false);
            mainTabbedPanel.setSelectedIndex(0);
        }
        passwordField.setText("");
    }//GEN-LAST:event_loginButtonMouseClicked


    private void regButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_regButtonMouseClicked
        // TODO add your handling code here:
        //1. get selection
        int currentRow = courseTable.getSelectedRow();
        //2. drop or reg
        boolean dropCourse;
        String sDropCourse = (String)courseTable.getValueAt(currentRow, 3);
        if(sDropCourse.equals("true"))
        {
            dropCourse = true;
        }
        else
        {
            dropCourse = false;
        }
        
        //3. coursename
        String courseName = (String) courseTable.getValueAt(currentRow, 0);
        //4. update database
        uciDataBase.registerCourse(courseName, dropCourse);
        //5. refresh tables
        refreshTables();
    }//GEN-LAST:event_regButtonMouseClicked

    private void refreshTables()
    {
        int remainingSeats;
        //clear contents
        for(int i=0;i<20;i++)
        {
            myCourseTable.setValueAt("", i, 0);
            myCourseTable.setValueAt("", i, 1);
            myCourseTable.setValueAt("", i, 2);
        }
        ArrayList<CourseStructure> allCourses = new ArrayList<>();
        ArrayList<CourseStructure> myCourses = new ArrayList<>();
        uciDataBase.listCourses(allCourses);
        uciDataBase.listStudentCourses(myCourses);
        int i = 0;
        for(CourseStructure oneCourse:allCourses)
        {
            courseTable.setValueAt(oneCourse.courseName, i, 0);
            remainingSeats = Integer.parseInt(oneCourse.availableSeats);
            if(remainingSeats > oneCourse.regStudents.size())
            {
                remainingSeats = remainingSeats - oneCourse.regStudents.size();
            }
            else
            {
                remainingSeats=0;
            }
            courseTable.setValueAt(String.valueOf(remainingSeats), i, 2);
            courseTable.setValueAt(oneCourse.courseSchedule, i, 1);
            courseTable.setValueAt(Boolean.toString(oneCourse.regged), i, 3);
            i = i + 1;
        }
        i = 0;
        for(CourseStructure oneCourse:myCourses)
        {
            myCourseTable.setValueAt(oneCourse.courseName, i, 0);
            myCourseTable.setValueAt(oneCourse.courseSchedule, i, 1);
            if(oneCourse.regStudents.size()>Integer.valueOf(oneCourse.availableSeats))
            {
                myCourseTable.setValueAt("Yes", i, 2);
            }
            else
            {
                myCourseTable.setValueAt("No", i, 2);
            }
            i = i + 1;
        }
        courseTable.repaint();
        myCourseTable.repaint();
    }
    
